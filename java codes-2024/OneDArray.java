import java.util.*;
public class OneDArray {

    public static void main(String[] args) {
        int s[] = new int[5];
Scanner sc = new Scanner(System.in);
        for(int i=0;i<s.length;i++)
        {
            s[i]=sc.nextInt();
        }

System.out.println("elements in arrays");

        for(int i=0;i<s.length;i++)
        {
            System.out.println(s[i]);
        }
    }
    
}
